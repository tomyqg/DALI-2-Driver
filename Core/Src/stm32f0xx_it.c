/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
	if(report_time > 0)
	{
		report_time--;
	}
	if(dead_time > 0)
	{
		dead_time--;
	}
	if(id_time > 0)
	{
		id_time--;
		if(id_time == 0)
			writePin(LED_Pin, 1);
	}
	if(adc_time > 0)
	{
		adc_time--;
		if(adc_time == 0)
			adc_flag = 1;
	}
	if(powerNoti_time > 0)
	{
		powerNoti_time--;
		if(powerNoti_time == 0)
			powerNoti_flag = 1;
	}
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */

  /* USER CODE END SysTick_IRQn 1 */
}

